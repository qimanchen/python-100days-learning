#!/usr/bin/env python3
# -*- coding: utf-8 -*-

import turtle

turtle.pensize(4)		# 绘制一个箭头，设置其大小
turtle.pencolor('red')	# 设置其颜色
turtle.forward(100)		# 向前移动100单位距离
turtle.right(90)	# 箭头方向顺时针方向转动90度
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.mainloop()